public class Edge {
    int from;       //Important
    int to;         //Important
    int runTime;    //Important
    int length;
    int speed;

    public Edge(int from, int to, int runTime, int length, int speed) {
        this.from = from;
        this.to = to;
        this.runTime = runTime;
        this.length = length;
        this.speed = speed;
    }


}
